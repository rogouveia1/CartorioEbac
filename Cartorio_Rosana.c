#include <stdio.h>//biblioteca de comunicação com o usuário
#include <stdlib.h>//biblioteca de alocação de espaço de memória
#include <locale.h>//biblioteca de alocação de texto por região
#include <string.h>//biblioteca responsável por cuitar das string

int Registro() //Função respnsável de cadastrar o usuário no sistema
{
	//inicio criação de variáves/string
	char arquivo[40];
	char cpf[40];
	char nome [40];
	char sobrenome [40];
	char cargo [40];
	//final criação de variáves/string
	
	printf("Digite o cpf a ser cadastrado: "); //coletando informações do usuário
	scanf("%s", cpf); //%s usado para salvar uma string
	
	strcpy(arquivo, cpf); //Responsável por copiar os valores das string
	
	FILE *file; //cria o arquivo
	file = fopen(arquivo, "w"); //cria o arquivo e o "w" significa escrever
	fprintf(file,cpf); //salva o valor da variável
	fclose(file); //fecha o arquivo
	
	file = fopen(arquivo, "a"); //atualiza
	fprintf(file, ","); //separa por virgulas
	fclose(file);
	
	printf("Digite o nome a ser cadastrado: ");
	scanf("%s",nome);
	
	file = fopen(arquivo, "a"); 
	fprintf(file,nome); 
	fclose(file); 
	
	file = fopen(arquivo, "a"); 
	fprintf(file,","); 
	fclose(file);
	
	printf("Digite o sobrenome a ser cadastrado: ");
	scanf("%s",sobrenome);
	
	file = fopen(arquivo, "a"); 
	fprintf(file,sobrenome); 
	fclose(file); 
	
	file = fopen(arquivo, "a"); 
	fprintf(file,","); 
	fclose(file);
	
	printf("Digite o cargo a ser cadastrado: ");
	scanf("%s",cargo);
	
	file = fopen(arquivo, "a"); 
	fprintf(file,cargo); 
	fclose(file);
		system("pause");
			
}

int Consulta()
{
	setlocale(LC_ALL, "Portuguese"); //Definindo a linguagem
	
	char cpf[40];
	char conteudo[200];
	
	printf("Digite o cpf a ser consultado: ");
	scanf("%s",cpf);
	
	FILE *file;	
	file = fopen(cpf,"r"); //ler o arquivo
	
	if(file == NULL)
	{
		printf("Não foi possível abrir o arquivo, não localizado!.\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usuário: "); 
		printf("%s", conteudo);
		printf("\n\n");	
	}
	
		system("pause");		
}

int Deletar()
{
	char cpf[40];
	printf("Digite o cpf do usuário a ser deletado: ");
	scanf("%s",cpf);
	
	remove(cpf);
	printf("O usuário foi deletado com sucesso!.\n");
	system("pause");
	
	FILE *file;	
	file = fopen(cpf,"r"); //ler o arquivo
	
	if(file == NULL)
	{
		
		printf("O usuário não se encontra no sistema!.\n");
	}
		system("pause");	
}

